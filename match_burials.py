# Build reverse alias map
alias_map = {}
for canon, variants in ALIASES.items():
    for v in variants:
        alias_map[v] = canon

def canonicalize_street(s):
    for variant, canon in alias_map.items():
        s = re.sub(r'\b' + re.escape(variant) + r'\b', canon, s)
    return s

