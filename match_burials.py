# Build lookup: normalized address string -> (pid, building_id, occ_addr)
def norm(s):
    if not s: return ''
    s = s.lower().strip()
    s = re.sub(r'\bпр[-\s]?кт\.?\b|\bпроспект\b', 'пр', s)
    s = re.sub(r'\bул\.?\b|\bулица\b', 'ул', s)
    s = re.sub(r'\bб[-\s]?р\.?\b|\bбульвар\b', 'бр', s)
    s = re.sub(r'\bпер\.?\b|\bпереулок\b', 'пер', s)
    s = re.sub(r'\bпл\.?\b|\bплощадь\b', 'пл', s)
    s = re.sub(r'\bнаб\.?\b|\bнабережная\b', 'наб', s)
    s = re.sub(r'[,\s]+', ' ', s).strip()
    return s

prop_index = []
for pid, bid, prewar, occ in props:
    for addr in [occ, prewar]:
        if addr:
            prop_index.append((norm(addr), pid, bid, occ or prewar))

