def extract_addr(line):
    """Return list of (street, house) tuples extracted from line."""
    # Remove header tokens
    line = re.sub(r'^(Могил[ыа]?|Тела?|Хоронят|Братские могилы|Свежие могилы|Эксгумация|Останки|Череп|Собака|Z-танки|Мертвая|Первые погибшие|Импровизированный морг)\s+[\dч\.\s]+\.?\s*', '', line, flags=re.I)
    line = re.sub(r'\s*\(.*?\)', '', line)  # remove parenthetical notes
    line = re.sub(r'^\s*\d+ч\.\s*', '', line)  # remove "Nч." count prefix
    
    results = []
    for m in ADDR_RE.finditer(line):
        prefix = (m.group(1) or '').lower().strip('.')
        street = m.group(2).strip().rstrip(',').strip()
        house = m.group(3).strip()
        # Skip obvious non-addresses
        if re.match(r'^\d+$', street): continue
        if len(street) < 3: continue
        if any(kw in street.lower() for kw in ['фото', 'видео', 'февраля', 'апреля', 'мая', 'июня', 'июля']): continue
        results.append((street.lower(), house.lower()))
    return results

# Build normalized spine index
spine = {}
for pid, bid, prewar, occ in props:
    for addr in [occ, prewar]:
        if not addr: continue
        n = canonicalize_street(norm(addr))
        spine[n] = (pid, bid, occ)

spine_keys = list(spine.keys())

# ── Match ───────────────────────────────────────────────────────────────────
SCORE_THRESHOLD = 82

results = []
for line in RAW.strip().splitlines():
    line = line.strip()
    if not line: continue
    
    addrs = extract_addr(line)
    if not addrs:
        results.append((line, [], []))
        continue
    
    line_matches = []
    for street, house in addrs:
        query = canonicalize_street(f'{street} {house}')
        hits = process.extract(query, spine_keys, scorer=fuzz.WRatio, limit=3, score_cutoff=SCORE_THRESHOLD)
        for key, score, _ in hits:
            pid, bid, occ_addr = spine[key]
            line_matches.append((score, pid, bid, occ_addr, query))
    
    line_matches.sort(reverse=True)
    seen_pids = set()
    deduped = []
    for score, pid, bid, occ_addr, query in line_matches:
        if pid not in seen_pids:
            deduped.append((score, pid, bid, occ_addr, query))
            seen_pids.add(pid)
    
    results.append((line, addrs, deduped))

# Print results
matched = 0
for line, addrs, hits in results:
    if hits:
        matched += 1
        print(f'ENTRY: {line}')
        for score, pid, bid, occ_addr, query in hits[:2]:
            print(f'  MATCH [{score:.0f}] pid={pid} | {occ_addr} | query={query}')
        print()

print(f'--- {matched} entries with spine matches ---')
